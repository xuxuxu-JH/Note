print("**********运算符_加减乘除取余**********")
-- lua中数值样式的字符串可以直接参与运算,lua会自动转成number类型
print("11" + 1)

-- lua中的除法,如果不能整除,会得到小数,不会对结果进行向下取整,C#会进行向下取整
