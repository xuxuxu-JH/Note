print("13" / 2)

-- 取余
print("13" % 2)

-- 幂运算
print("幂运算" .. 2 ^ 2) -- 2的2次方

print("**********条件运算符**********")
-- 分别是 > < >= <= == ~=
-- >注意:不等号是~=

-- 逻辑与 和逻辑或
-- 逻辑与是and 逻辑或是or

